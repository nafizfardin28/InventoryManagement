package com.ecommerce.report;

public class Report {
    public String title;
    public String subTitle;

    String name;
    String phoneNumber;
    String id;
    int totalOrders;

    public String getTitle() {
        return title;
    }

    public String getName() {
        return name;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public String getId() {
        return id;
    }

    public int getTotalOrders() {
        return totalOrders;
    }
    public String getSubTitle() {
        return subTitle;
    }
}
